import mongoose from 'mongoose';

// Schema for skills the user has mastered and can teach others
const MasteredSkillSchema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true
  },
  description: {
    type: String,
    default: '',
    trim: true
  },
  experienceYears: {
    type: Number,
    default: 0,
    min: 0,
    max: 100
  },
  createdAt: {
    type: Date,
    default: Date.now
  }
});

export default MasteredSkillSchema; 